// Helper functions for use in JavaScript
export const getThemeColor = (color, shade = 600) => {
  return colors[color]?.[shade] || colors.primary[600];
};

export const getFontSize = (size = 'base') => {
  return typography.fontSize[size] || typography.fontSize.base;
};

export const getSpacing = (size = 4) => {
  return spacing[size] || spacing[4];
};

export const getBreakpoint = (size = 'md') => {
  return breakpoints[size] || breakpoints.md;
};

// Media query helper for styled-components or inline styles
export const mediaQuery = (breakpoint, content) => {
  return `@media (min-width: ${getBreakpoint(breakpoint)}) { ${content} }`;
};

